<h4>What is DataViz?</h4>
<p>DataViz is an interactive visualization library built on top of mobstick. The interface supports seamless transition from one visualization type to another.</p>

<h4>Yet another vizkit?</h4>
<p>Yes you heard me. Yet another vizkit. Why did I not just stick to d3 or protovis or the wellknown fusion charts? There is just one simple reason to it. As good and structured
as they are they are static charts and to have an interactive behaviour built on them would be a tedious ask. I looked at mobstack, d3 and fusioncharts and decided to pick d3 and mobstack to build an interactive layer on top of it. Along the process I have written some javascript libraries to help me ease through</p>

<h4>How does it work?</h4>
<p>It works by consuming a certain json and producing the required visualization. you can seemelessly transition from one viz. to another. The data could very well come from your server.</p>

<h4>Work in progress</h4>
<p>Creating custom dashboards and widgetifying charts is a work in progress. The idea is to have custom dashboards created and have them saved locally on the disk or on some file server and retrieve and present as and when they are needed</p>
